SELECT DISTINCT I.DESCRIPTION, I.INVENTORY_ON_HAND, S.INITIAL_COST, (INITIAL_COST * INVENTORY_ON_HAND) AS TOTAL_PURCHASE_COST
FROM INVENTORY I, SUPPLIES_AND_SUPPLIERS S
WHERE I.PRODUCT_NUM = S.PRODUCT_NUM;

SELECT DESCRIPTION, SUPPLIER_NAME, SUPPLIER_PHONE_NUMBER, INITIAL_PURCHASE_DATE, 
