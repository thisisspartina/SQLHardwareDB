CREATE TABLE INVENTORY
(ITEM_NUM VARCHAR(4) PRIMARY KEY,
DESCRIPTION VARCHAR(30),
SIZE VARCHAR(15),
INVENTORY_ON_HAND INT,
COST_PER_DAY DECIMAL(20,2),
PRODUCT_NUM VARCHAR(25) );
SELECT *
FROM INVENTORY;

CREATE TABLE SUPPLIES_AND_SUPPLIERS
(PRODUCT_NUM VARCHAR(25) PRIMARY KEY,
DESCRIPTION VARCHAR(30),
INITIAL_COST DECIMAL(8,2),
INITIAL_PURCHASE_DATE DATETIME,
REPLACEMENT_DATE DATETIME,
SUPPLIER_NAME VARCHAR(25),
SUPPLIER_PHONE_NUMBER VARCHAR(20) );
SELECT *
FROM SUPPLIES_AND_SUPPLIERS;

CREATE TABLE CUSTOMER_INFO
(CUSTOMER_NUM VARCHAR(5) PRIMARY KEY,
FIRST_NAME VARCHAR(20),
LAST_NAME VARCHAR(25),
ADDRESS VARCHAR(30),
TIMES_RENTED INT,
INITIAL_RENTAL_DATE DATETIME,
LAST_RENTAL_DATE DATETIME,
REFERED_BY VARCHAR(20),
CLERK_NUM VARCHAR(3),
DICOUNT_LEVEL CHAR(1),
PRODUCT_NUM VARCHAR(25) );
SELECT *
FROM CUSTOMER_INFO;

CREATE TABLE EMPLOYEE_INFO
(CLERK_NUM VARCHAR(3) PRIMARY KEY,
SECURITY_LVL CHAR(2),
FIRST_NAME VARCHAR(20),
LAST_NAME VARCHAR(25),
ADDRESS VARCHAR(30),
HIRE_DATE DATETIME,
TERMINATION_DATE DATETIME,
TERMINATION_CODE CHAR(2),
PHONE_NUM VARCHAR(20) );
SELECT *
FROM EMPLOYEE_INFO;

